export const AVAILABLE_THEMES = Object.freeze(Object.keys(THEME_PRESETS));
export const ORDERED_THEMES = Object.freeze(
  [...AVAILABLE_THEMES].sort((leftThemeName, rightThemeName) => {
    const leftTheme = THEME_PRESETS[leftThemeName];
    const rightTheme = THEME_PRESETS[rightThemeName];
    if (leftTheme.sortGroup !== rightTheme.sortGroup) {
      return leftTheme.sortGroup - rightTheme.sortGroup;
    }
    if (leftTheme.sortIndex !== rightTheme.sortIndex) {
      return leftTheme.sortIndex - rightTheme.sortIndex;
    }
    return leftTheme.label.localeCompare(rightTheme.label, "tr", { sensitivity: "base" });
  }),
);

export const THEME_VARIABLE_NAMES = Object.freeze(Object.keys(BASE_THEME_VARIABLES));

export function getThemePreset(themeName) {
  return THEME_PRESETS[themeName] || THEME_PRESETS.light;
}
